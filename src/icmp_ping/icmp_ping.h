#ifndef _ICMP_PING_H_
#define _ICMP_PING_H_

#define REQUEST_PKT_LEN 64  /* 请求包的大小 */
#define RESONSE_PKT_LEN 256 /* 回应包的大小 */

/***************************************************************/
/* 函  数：ping *************************************************/
/* 说  明：ping的主程序 ******************************************/
/* 参  数：ip地址 ***********************************************/
/*      ：times = -1 一直请求 ***********************************/
/*      ：times = 0 默认请求次数 *********************************/
/*      ：times = 其他请他请求次数 ********************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void ping(char *ip, int times);

/***************************************************************/
/* 函  数：ping_reset *******************************************/
/* 说  明：恢复ping的一些默认参数 **********************************/
/* 参  数：无 ***************************************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void ping_reset();

/***************************************************************/
/* 函  数：set_ping_times ***************************************/
/* 说  明：设置ping的默认次数 *************************************/
/* 参  数：times 次数 ********************************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void set_ping_times(int times);

/***************************************************************/
/* 函  数：ping_usage *******************************************/
/* 说  明：介绍ping使用方法 ***************************************/
/* 参  数：无 ***************************************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void ping_usage();

#endif
