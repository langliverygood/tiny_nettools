#ifndef _LSHELL_H_
#define _LSHELL_H_

#define RUN_AT_NEW_THREAD  0 /* 在新线程中执行命令 */
#define RUN_AT_MAIN_THREAD 1 /* 在主线程中执行命令 */

/***************************************************************/
/* 函  数：lshell_init ******************************************/
/* 说  明：lshell初始化 ******************************************/
/* 参  数：无 ***************************************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void lshell_init();

/***************************************************************/
/* 函  数：lshell_set_promt *************************************/
/* 说  明：设置提示符 *********************************************/
/* 参  数：str 提示符 ********************************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void lshell_set_promt(const char *str);

/***************************************************************/
/* 函  数：lshell_set_errmsg_swtich *****************************/
/* 说  明：设置错误提示开关 ****************************************/
/* 参  数：0 关闭错误提示 *****************************************/
/*        1 打开错误提示 *****************************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void lshell_set_errmsg_swtich(int flag);

/***************************************************************/
/* 函  数：lshell_register **************************************/
/* 说  明：注册用户的命令 *****************************************/
/* 参  数：parent 父命令id,若无父命令,则为-1 ***********************/
/*        cmd 命令 *********************************************/
/*        func 函数指针 *****************************************/
/*        mode 函数执行方式,0 在新线程,1 在主线程 ******************/
/* 返回值：该命令的id，若出错则返回-1 ******************************/
/**************************************************************/
int lshell_register(int parent, const char *cmd, const char *tip, void (* func)(int argc, char **argv), int mode);

/***************************************************************/
/* 函  数：lshell_start *****************************************/
/* 说  明：lshell启动 *******************************************/
/* 返回值：无 ***************************************************/
/**************************************************************/
void lshell_start();

#endif
